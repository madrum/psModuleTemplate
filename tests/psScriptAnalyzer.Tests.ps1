<#
.NOTES
    * PSScriptAnalyzer is used to "lint" PowerShell scripts, which means to check for programmatic and stylistic issues.
    * It is good practice to perform these checks on a regular basis and in an automated fashion, like when checking in code or on pull requests.
    * Get functions in PSScriptAnalyzer module: get-command -module psscriptanalyzer
    * Get rules: Get-ScriptAnalyzerRule
